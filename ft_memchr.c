#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char *str;

	str = (unsigned char *)s;
	while ((n--) >= 0)
	{
		if (str[n] == (unsigned char)c)
			return ((char *)s + n);
	}
	return (NULL);
}
