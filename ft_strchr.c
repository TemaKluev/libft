#include "libft.h"
#include <stdio.h>

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i++;
	}
	if (s[i] == (char)c)
		return ((char *)s + i);
	else
		return (NULL);
}

int main(void)
{
	char *s = "facasdfar4gawersdfgwef2g";
	int c = '4';

	printf ("%s\n", ft_strchr(s, c));
}