#include "libft.h"

char	*ft_substr(const char *s, unsigned int start, size_t len)
{
	char	*str;
	size_t	i;

	str = malloc(sizeof(char) * len + 1);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = s[start];
		i++;
		start++;
	}
	str[i] = '\0';
	return (str);
}

#include <stdio.h>

int main(void)
{
	const char *s = "hello everyone\n";
	char *sub = ft_substr(s, 7, 40);

	printf("%s", sub);

	return (0);
}